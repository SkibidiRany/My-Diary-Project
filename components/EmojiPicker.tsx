export default function EmojiPicker({
  visible,
  onEmojiSelect,
  onClose,
  selectedEmoji,
  title = 'Choose an Emoji',
}: EmojiPickerProps) {
  const [selectedCategory, setSelectedCategory] = useState(0);

  const filteredEmojis = useMemo(() => {
    return EMOJI_CATEGORIES[selectedCategory].emojis;
  }, [selectedCategory]);

  const handleEmojiSelect = (emoji: string) => {
    onEmojiSelect(emoji);
    onClose();
  };

  const renderEmojiItem = ({ item }: { item: string }) => (
    <TouchableOpacity
      style={[
        styles.emojiItem,
        selectedEmoji === item && styles.selectedEmojiItem,
      ]}
      onPress={() => handleEmojiSelect(item)}
    >
      <Text style={styles.emojiText}>{item}</Text>
    </TouchableOpacity>
  );

  const renderCategoryTab = (category: EmojiCategory, index: number) => {
    const isSelected = selectedCategory === index;
    return (
      <TouchableOpacity
        key={index}
        style={[
          styles.categoryTab,
          isSelected && styles.selectedCategoryTab,
        ]}
        onPress={() => setSelectedCategory(index)}
      >
        <Text style={styles.categoryIcon}>{category.icon}</Text>
      </TouchableOpacity>
    );
  };

  return (
    <Modal
      visible={visible}
      animationType="slide"
      presentationStyle="pageSheet"
      onRequestClose={onClose}
    >
      <SafeAreaView style={styles.container}>
        {/* Header */}
        <View style={styles.header}>
          <Text style={styles.title}>{title}</Text>
          <TouchableOpacity style={styles.closeButton} onPress={onClose}>
            <Text style={styles.closeButtonText}>Done</Text>
          </TouchableOpacity>
        </View>

        {/* Category Tabs */}
        <View style={styles.categoryTabsWrapper}>
          <ScrollView
            horizontal
            showsHorizontalScrollIndicator={false}
            contentContainerStyle={styles.categoryTabsContent}
          >
            {EMOJI_CATEGORIES.map(renderCategoryTab)}
          </ScrollView>
        </View>

        {/* Emoji Grid */}
        <FlatList
          data={filteredEmojis}
          renderItem={renderEmojiItem}
          keyExtractor={(item, index) => `${item}-${index}`}
          numColumns={8}
          style={styles.emojiGrid}
          contentContainerStyle={styles.emojiGridContent}
          showsVerticalScrollIndicator={false}
          ListEmptyComponent={
            <View style={styles.emptyContainer}>
              <Text style={styles.emptyText}>No emojis found</Text>
            </View>
          }
        />

        {/* Current Category Label */}
        <View style={styles.categoryLabel}>
          <Text style={styles.categoryLabelText}>
            {EMOJI_CATEGORIES[selectedCategory].name}
          </Text>
        </View>
      </SafeAreaView>
    </Modal>
  );
}

